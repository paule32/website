<p>
<b><font color="blue">Beschreibung</font></b>
</p>
<div style="height:32vh"">
Scope entstammt aus dem englischen. Es bedeutet soviel wie: "innerhalb eines Bereichs".<br>
Als Beschreibung oder Bezeichnung ist damit gemeint, dass Objekte innerhalb und außerhalb
unterschiedlich Werte haben können, sofern sie auch definiert wurden.<br>
Es gibt:
<ul>
  <li>global scope's,</li>
  <li>local scope's.</li>
</ul>
</div>